#  to Octal also one method is there i.e oct(x), where that x can be of any base

# decimal to octal
number = 10
print('the decimal : {} in octal : {}'.format(number, oct(number)))

# from binary to octal
number = 0B01011001
print('the binary : {} in octal : {}'.format(number, oct(number)))

# from hexadecimal to octal
number = 0X1049cda32D
print('the hexa-decimal : {} in octal : {}'.format(number, oct(number)))