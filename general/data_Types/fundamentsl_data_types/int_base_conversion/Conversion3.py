# for converting to hexa- decimal one method is there i.e hex(x), where x can be any base

# from decimal to hex
number = 10;
print('the decimal number {} in hexa decimal is : {}'.format(number, hex(number)))

# from binary to hex
number = 0b10110010
print('the binary number {} in hexa decimal is : {}'.format(number, hex(number)))

# from octal to hex
number = 0O4573
print('the octal number {} in hexa decimal is : {}'.format(number, hex(number)))